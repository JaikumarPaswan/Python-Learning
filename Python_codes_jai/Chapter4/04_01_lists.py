#Create a list using []
a = [1, 2, 46, 82, 7]

#Print the list using print() function
print(a)

#Access using index a[0], a[1]
print(a[2])

#Change the value in list 
a[0] = 98
print(a)

#We can create a list with items of different types
c = [45, "jai", False, 6.9]
print(c)
print(type(c[2]))

#Reverse a list
print(a[::-1])

